public class CharArray {
    public static void main(String[] args) {
        char array[] = new char[4];

        array[0] = 'j';
        array[1] = 'e';
        array[2] = 's';
        array[3] = 's';

        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}