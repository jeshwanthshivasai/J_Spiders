package JAVA_PRACTICE_W3.Basic_Part_1;

public class Compare_Two_Numbers {
	public static void main(String[] args) {
		int a = 25;
		int b = 39;
		if(a!=b) {
			System.out.println("25 != 39");
		} else if (a>b) {
			System.out.println("25 < 39");
		} else if (a <= b) {
			System.out.println("25 <= 39");
		}
	}
}
