public class SwitchCaseEx {
    public static void main(String[] args) {
        int a = 4;
        switch (a) {
            case 1: System.out.println("Hello");
            break;
            case 2: System.out.println("World!");
            break;
            case 3: System.out.println("I'm");
            break;
            case 4: System.out.println("a");
            break;
            case 5: System.out.println("Human");
            break;
        }
    }
}